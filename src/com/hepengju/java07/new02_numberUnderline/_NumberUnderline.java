package com.hepengju.java07.new02_numberUnderline;

import org.junit.Test;

/**
 * 数字下划线
 * 
 * <br> 说明: 编译时编译器自己主动删除数字中的下划线
 * @author hepengju
 *
 */
public class _NumberUnderline {

    @Test public void testNumberUnderline() {
        int m = 100_0000;      // 一百万
        int n = 1_0000_0000;   // 一亿
        System.out.println(m); // 1000000
        System.out.println(n); // 100000000
    }
}
